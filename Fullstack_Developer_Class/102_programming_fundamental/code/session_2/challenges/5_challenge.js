// Saat ini, jumlah usia Andi & Budi = 49 th, dengan rasio Usia Andi & Budi = 0.4.
// Berapa usia Andi & Budi 2 tahun lagi?

// rasio 0.4 = 4 : 10 = 2 : 5

let jumlahUsiaAndiDanBudi = 49;
let usiaAndi = (2 / 7) * jumlahUsiaAndiDanBudi;
let usiaBudi = (5 / 7) * jumlahUsiaAndiDanBudi;

console.log("Usia Andi saat ini = ", usiaAndi);
console.log("Usia Budi saat ini = ", usiaBudi);

console.log("Usia Andi 2 tahun kemudian = ", usiaAndi + 2);
console.log("Usia Budi 2 tahun kemudian = ", usiaBudi + 2);
